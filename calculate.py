""" 
数の計算で使う演算子
"""

# 足し算
print("1+2={}".format(1+2))
# 引き算
print("5-1={}".format(5-1))
# 掛け算
print("3×4={}".format(3*4))
# 割り算
print("4÷2={}".format(4/2))
# 割り算（整数部）
print("6÷5の整数値は{}".format(6//5))
# 剰余
print("5÷3の余りは{}".format(5%3))
# べき乗
print("5の3乗は{}".format(5**3))


""" 
代入演算子
"""
# 足し算
a = 5
a += 2
print("5+2={}".format(a))
# 引き算
b = 15
b -= 10
print("15-10={}".format(b))
# 掛け算
c = 9
c *= 3
print("9×3={}".format(c))
# 割り算
d = 10
d /= 3
print("10÷3={}".format(d))
# 割り算（整数部）
e = 10
e //= 3
print("10÷3の整数値は{}".format(e))
# 剰余
f = 10
f %= 3
print("10÷3の余りは{}".format(f))
# べき乗
g = 2
g **= 10
print("2の10乗は{}".format(g))

